public class Rechnen {
    public static void main(String[] args) {
        int zahl1 = 3;
        int zahl2 = 5;
        int erg = zahl1 + zahl2;

        System.out.println(zahl1 + "+" + zahl2 + "=" + erg);
        erg = zahl2 - zahl1;
        System.out.println(zahl2 + "-" + zahl1 + "=" + erg);
        erg = zahl2 * zahl1;
        System.out.println(zahl2 + "*" + zahl1 + "=" + erg);
        double zahl3 = 3;
        double erg1 = zahl2 / zahl3;
        erg1 = zahl2 / zahl3;
        System.out.println(zahl2 + "/" + zahl1 + "=" + erg1);
        erg = zahl2%zahl1;
        System.out.println(zahl2 + "%" + zahl1 + "=" + erg);
        System.out.println(zahl2 + "++ =" + ++zahl2);
        System.out.println(zahl2 + "-- =" + --zahl2);


    }
}
